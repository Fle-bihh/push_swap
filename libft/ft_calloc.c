#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ret;

	ret = malloc(size * count);
	if (!(ret))
		ft_error_malloc();
	ft_memset(ret, 0, size * count);
	return (ret);
}
